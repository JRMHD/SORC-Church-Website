        $(document).ready(function() {
            $("#subscribeForm").submit(function(event) {
                event.preventDefault();

                // Clear previous messages
                $("#responseMessage").html("");

                // Enable loading state
                $("button[type='submit']").addClass("loading-state");
                $("#buttonLoader").show();
                $("#buttonText").text("Sending...");
                $("button[type='submit']").prop("disabled", true);

                // Log to console for debugging
                console.log("Form submitted, showing loader");

                $.ajax({
                    url: "{{ route('newsletter.subscribe') }}",
                    type: "POST",
                    data: $(this).serialize(),
                    dataType: "json",
                    success: function(response) {
                        console.log("Success response received");
                        setTimeout(function() {
                            // Disable loading state
                            $("#buttonLoader").hide();
                            $("#buttonText").text("Subscribe");
                            $("button[type='submit']").removeClass("loading-state");
                            $("button[type='submit']").prop("disabled", false);

                            // Show success message
                            $("#responseMessage").html(
                                '<div class="alert alert-success fade-in">' +
                                '<i class="fa fa-check-circle mr-2"></i>' + response
                                .success + '</div>');
                            $("#subscribeForm")[0].reset();
                        }, 600);
                    },
                    error: function(xhr) {
                        console.log("Error response received");
                        setTimeout(function() {
                            // Disable loading state
                            $("#buttonLoader").hide();
                            $("#buttonText").text("Subscribe");
                            $("button[type='submit']").removeClass("loading-state");
                            $("button[type='submit']").prop("disabled", false);

                            // Show error message
                            let errors = xhr.responseJSON.errors;
                            let errorMessage =
                                '<div class="alert alert-danger fade-in">' +
                                '<i class="fa fa-exclamation-circle mr-2"></i>';

                            if (errors) {
                                $.each(errors, function(key, value) {
                                    errorMessage += value[0] + "<br>";
                                });
                            } else {
                                errorMessage +=
                                    "Something went wrong. Please try again.";
                            }

                            errorMessage += '</div>';
                            $("#responseMessage").html(errorMessage);
                        }, 600);
                    }
                });
            });
        });
    </script>

